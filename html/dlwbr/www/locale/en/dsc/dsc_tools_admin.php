<h1>Administrator Settings</h1>
There are two accounts that can access the router's management interface. These accounts are <strong>admin</strong> and <strong>user</strong>.<br><br>

<strong>Admin</strong> has read/write access while <strong>user</strong> has read-only access.<br> 
<strong>User</strong> can only view the settings but cannot make any changes.<br>
Only the <strong>admin</strong> account has the ability to change both <strong>admin</strong> and <strong>user</strong> account passwords.<br><br>

